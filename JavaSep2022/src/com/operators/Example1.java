package com.operators;

public class Example1 {

	public static void main(String[] args){
		int a = 30;
		int b= 20;
		
		int result = a + b;
		System.out.println(result);
		
		int sub = a - b;
		System.out.println(sub);
		
		int multiple = a * b;
		System.out.println(multiple);
		
		int div = a / b;
		System.out.println(div);
		
		int rem = a % b;
		System.out.println(rem);
	}
}

